    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            double gia = Double.parseDouble(txtGia.getText());

            Thuoc t = new Thuoc();
            t.setTenThuoc(txtTenThuoc.getText());
            t.setHangSX(txtHangSX.getText());
            t.setNgaySX(txtNgaySX.getText());
            t.setHanSD(txtHanSD.getText());
            t.setGia(gia);

            int generatedMaThuoc = new ThuocDAO().addThuoc(t);
            if (generatedMaThuoc != -1) {
                t.setMaThuoc(generatedMaThuoc);

                DefaultTableModel model = (DefaultTableModel) tblThuoc.getModel();
                model.addRow(new Object[]{
                    t.getMaThuoc(),
                    t.getTenThuoc(),
                    t.getHangSX(),
                    t.getNgaySX(),
                    t.getHanSD(),
                    t.getGia()
                });

                // Xóa trắng các trường nhập liệu
                txtTenThuoc.setText("");
                txtHangSX.setText("");
                txtNgaySX.setText("");
                txtHanSD.setText("");
                txtGia.setText("");
                
                JOptionPane.showMessageDialog(this, "Thêm thuốc thành công với mã: " + generatedMaThuoc);
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thuốc thất bại", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Giá phải là số hợp lệ", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:      
    // Kiểm tra nếu chưa chọn thuốc trong bảng
    int selectedRow = tblThuoc.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(rootPane, "Chọn thuốc để sửa!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Lấy ID thuốc từ hàng được chọn trong bảng
        int maThuoc = (int) tblThuoc.getValueAt(selectedRow, 0); // Giả sử cột đầu tiên là ID thuốc

        if (txtTenThuoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Tên thuốc không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtHangSX.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Hãng sản xuất không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtNgaySX.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ngày sản xuất không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtHanSD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Hạn sử dụng không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Giá không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Chuyển đổi giá trị giá từ txtGia sang kiểu double
        double gia = Double.parseDouble(txtGia.getText());

        // Tạo đối tượng Thuoc và thiết lập thông tin
        Thuoc t = new Thuoc();
        t.setMaThuoc(maThuoc); // Thiết lập ID thuốc cần cập nhật
        t.setTenThuoc(txtTenThuoc.getText());
        t.setHangSX(txtHangSX.getText());
        t.setNgaySX(txtNgaySX.getText());
        t.setHanSD(txtHanSD.getText());
        t.setGia(gia);

        // Gọi phương thức update trong DAO để cập nhật thuốc
        if (new ThuocDAO().updateThuoc(t)) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thuốc thành công");
            loadThuoc(); // Cập nhật dữ liệu trên JTable
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thuốc thất bại", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "Giá phải là số hợp lệ", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
    int selectedRow = tblThuoc.getSelectedRow();
    if (selectedRow == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon thuoc de xoa!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    int confirm = JOptionPane.showConfirmDialog(rootPane,
                "Bạn có chắc muốn xoá bản ghi này?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
    if (confirm == JOptionPane.YES_OPTION) {
            // Lấy ID của tài khoản cần xóa
            int maThuoc = (int) tblThuoc.getValueAt(selectedRow, 0); // Giả sử cột đầu tiên là ID

            // Gọi phương thức xóa trong DAO
            ThuocDAO dao = new ThuocDAO();
            if (dao.deleteThuoc(maThuoc)) {
                JOptionPane.showMessageDialog(rootPane, "Account deleted successfully!");

                // Cập nhật lại bảng
                loadThuoc();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed to delete account.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    
    }//GEN-LAST:event_btnXoaActionPerformed

