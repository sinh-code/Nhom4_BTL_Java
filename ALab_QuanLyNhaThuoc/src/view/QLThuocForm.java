    private void loadThuoc() {
        ThuocDAO thuocDAO = new ThuocDAO();
        ArrayList<Thuoc> danhSachThuoc = thuocDAO.getAllThuoc();

        DefaultTableModel model = (DefaultTableModel) tblThuoc.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ
        

        for (Thuoc t : danhSachThuoc) {
            model.addRow(new Object[]{
                t.getMaThuoc(),
                t.getTenThuoc(),
                t.getHangSX(),
                t.getNgaySX(),
                t.getHanSD(),
                t.getGia()
            });
        }
    }

    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tblThuoc.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trên bảng

        ThuocDAO dao = new ThuocDAO();
        thuoc = dao.getAllThuoc(); // Lấy danh sách tài khoản từ cơ sở dữ liệu

        if (thuoc == null || thuoc.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,
                    "No data found in the database. Please check your connection or add some accounts.",
                    "Information",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        // Thêm dữ liệu vào bảng
        for (Thuoc s : thuoc) {
            model.addRow(new Object[]{
                                s.getMaThuoc(),

                s.getTenThuoc(),
                s.getHangSX(),
                s.getNgaySX(),
                s.getHanSD(),
                s.getGia()
            });
        }
}


