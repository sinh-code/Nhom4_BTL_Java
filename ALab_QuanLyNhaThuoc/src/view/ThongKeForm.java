    private void btnSapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSapXepActionPerformed
        // TODO add your handling code here:
        txtTongDoanhThu.setText("");
boolean ascending;
if (btnTang.isSelected()) {
    ascending = true; // Sắp xếp tăng dần
} else if (btnGiam.isSelected()) {
    ascending = false; // Sắp xếp giảm dần
} else {
    // Nếu không chọn nút nào, hiển thị thông báo
    JOptionPane.showMessageDialog(this, "Vui lòng chọn kiểu sắp xếp (Tăng dần hoặc Giảm dần)!", "Thông báo", JOptionPane.WARNING_MESSAGE);
    return;
}
    try {
        ThongKeDAO thongKeDAO = new ThongKeDAO();
        
        // Get sorted invoices
        List<HoaDon> sortedHoaDonList = thongKeDAO.sortHoaDonByTongTien(ascending);

        // Clear the table model
        DefaultTableModel model = (DefaultTableModel) tblThongKe.getModel();
        model.setRowCount(0);

        // Populate the table with sorted data
        for (HoaDon hoaDon : sortedHoaDonList) {
            model.addRow(new Object[]{
                hoaDon.getMaHD(),
                hoaDon.getTenKH(),
                hoaDon.getNgayLap(),
                hoaDon.getSDT(),
                hoaDon.getDiaChi(),
                hoaDon.getTongTien()
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi sắp xếp hóa đơn!", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnSapXepActionPerformed

