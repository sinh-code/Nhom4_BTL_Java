    private void btnDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDateActionPerformed
        // TODO add your handling code here:
    String ngayLap = txtNgay.getText().trim();

    if (ngayLap.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        ThongKeDAO thongKeDAO = new ThongKeDAO();
        
        // Get total revenue for the date
        double tongDoanhThu = thongKeDAO.getTongDoanhThuTheoNgay(ngayLap);

        if (tongDoanhThu == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn hoặc doanh thu cho ngày: " + ngayLap, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            txtTongDoanhThu.setText(""); // Clear the field if no data is found
        } else {
            // Set the total revenue in the txtTongDoanhThu field
            txtTongDoanhThu.setText(String.format("%.2f", tongDoanhThu));
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        txtTongDoanhThu.setText(""); // Clear the field in case of an error
    }

    }//GEN-LAST:event_btnDateActionPerformed

